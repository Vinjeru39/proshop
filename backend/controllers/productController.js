import asyncHandler from "../middleware/asyncHandler.js";
import Product from "../models/productModel.js";

// @desc Fetch all products
// @route GET /api/products
// @access Public
const getProducts = asyncHandler(async (req, res) => {
  const pageSize = process.env.PAGINATION_LIMIT; //number of products per page
  const page = Number(req.query.pageNumber) || 1;

  const keyword = req.query.keyword
    ? { name: { $regex: req.query.keyword, $options: "i" } }
    : {}; //this way even if e.g. the keyword is phone and we have iphone in the shop it will stil match i.e. not direcly exact //options there for case insensitive

  //The page number thats in the url i.e. the req.query
  const count = await Product.countDocuments({ ...keyword }); //The total number of products

  const products = await Product.find({ ...keyword })
    .limit(pageSize)
    .skip(pageSize * (page - 1)); //Skip the pages that are before e.g. if we are on page 3 skip page 1 and 2
  res.json({ products, page, pages: Math.ceil(count / pageSize) });
});

